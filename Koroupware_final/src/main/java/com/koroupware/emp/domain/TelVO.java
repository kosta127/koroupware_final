package com.koroupware.emp.domain;

public class TelVO {
	private int tel_no;
	private int emp_no;
	private String tel_type;
	private String tel_telephone_left;
	private String tel_telephone_center;
	private String tel_telephone_right;
	public int getTel_no() {
		return tel_no;
	}
	public void setTel_no(int tel_no) {
		this.tel_no = tel_no;
	}
	public int getEmp_no() {
		return emp_no;
	}
	public void setEmp_no(int emp_no) {
		this.emp_no = emp_no;
	}
	public String getTel_type() {
		return tel_type;
	}
	public void setTel_type(String tel_type) {
		this.tel_type = tel_type;
	}
	public String getTel_telephone_left() {
		return tel_telephone_left;
	}
	public void setTel_telephone_left(String tel_telephone_left) {
		this.tel_telephone_left = tel_telephone_left;
	}
	public String getTel_telephone_center() {
		return tel_telephone_center;
	}
	public void setTel_telephone_center(String tel_telephone_center) {
		this.tel_telephone_center = tel_telephone_center;
	}
	public String getTel_telephone_right() {
		return tel_telephone_right;
	}
	public void setTel_telephone_right(String tel_telephone_right) {
		this.tel_telephone_right = tel_telephone_right;
	}
	
}
